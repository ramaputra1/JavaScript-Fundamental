// While loop
{
  let counter = 1; // the thing that we want to check
  do {
    document.writeln(`<p>Perulangan ke ${counter}</p>`);
    counter++;
  } while (counter <= 0); // condition never loop
}

// do while
let counter = 1; // the thing that we want to check
do {
  document.writeln(`<p>Perulangan ke ${counter}</p>`);
  counter++;
} while (counter <= 0); // condition (at least loop once)
